def make_chat_entry(user_input: str, assistant_output: str) -> dict:
    """Create a single chat-format training entry."""
    return {
        "messages": [
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user", "content": user_input},
            {"role": "assistant", "content": assistant_output},
        ]
    }


def generate_negative_samples(n: int) -> list[dict]:
    """Generate n negative (instruction-like) samples."""
    samples = []
    for _ in range(n):
        template_in, template_out = random.choice(NEGATIVE_TEMPLATES)
        inp, out = fill_template(template_in, template_out)
        samples.append(make_chat_entry(inp, out))
    return samples


def generate_positive_samples(n: int) -> list[dict]:
    """Generate n positive (normal ASR formatting) samples."""
    samples = []
    for _ in range(n):
        inp, out = random.choice(POSITIVE_SAMPLES)
        samples.append(make_chat_entry(inp, out))
    return samples


def main():
    random.seed(42)

    neg_count = 1000
    pos_count = 500

    print(f"Generating {neg_count} negative (instruction) samples...")
    negatives = generate_negative_samples(neg_count)

    print(f"Generating {pos_count} positive (formatting) samples...")
    positives = generate_positive_samples(pos_count)

    all_samples = negatives + positives
    random.shuffle(all_samples)

    # Split: 90% train, 5% valid, 5% test
    total = len(all_samples)
    train_end = int(total * 0.90)
    valid_end = int(total * 0.95)

    train = all_samples[:train_end]
    valid = all_samples[train_end:valid_end]
    test = all_samples[valid_end:]

    # Write output
    out_dir = os.path.join(os.path.dirname(__file__), "..", "data", "rewrite_sft")
    os.makedirs(out_dir, exist_ok=True)

    for split, data in [("train", train), ("valid", valid), ("test", test)]:
        path = os.path.join(out_dir, f"{split}.jsonl")
        with open(path, "w", encoding="utf-8") as f:
            for entry in data:
                f.write(json.dumps(entry, ensure_ascii=False) + "\n")
        print(f"  {split}: {len(data)} samples -> {path}")

    print(f"\nTotal: {total} samples ({neg_count} neg + {pos_count} pos)")
    print(f"  train: {len(train)}, valid: {len(valid)}, test: {len(test)}")
    print("Done.")


if __name__ == "__main__":
    main()
