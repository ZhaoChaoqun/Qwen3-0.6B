NUMS = ["3", "5", "10", "20", "50", "100", "200", "300", "500", "1000"]


def fill_template(inp: str, out: str) -> tuple[str, str]:
    """Fill in template placeholders with random values."""
    topic = random.choice(TOPICS)
    person = random.choice(PERSONS)
    num = random.choice(NUMS)
    inp = inp.replace("{topic}", topic).replace("{person}", person).replace("{num}", num)
    out = out.replace("{topic}", topic).replace("{person}", person).replace("{num}", num)
    return inp, out


