def print_stats():
    """打印用例分布统计。"""
    print(f"总用例数: {len(test_cases)}")
    categories = [
        ("商务与沟通", 0, 30),
        ("知识管理与写作", 30, 50),
        ("轻量级技术与工具", 50, 70),
        ("AI 交互指令", 70, 85),
        ("复杂数字与格式", 85, 100),
    ]
    for name, start, end in categories:
        actual = min(end, len(test_cases)) - start
        print(f"  {name}: {actual} 条 (#{start+1}-#{min(end, len(test_cases))})")

    # 能力覆盖统计
    abilities = {"标点": 0, "CSC": 0, "ITN": 0, "术语": 0, "排版": 0}
    for _, desc in test_cases:
        desc_lower = desc.lower()
        if "标点" in desc_lower:
            abilities["标点"] += 1
        if "csc" in desc_lower:
            abilities["CSC"] += 1
        if "itn" in desc_lower:
            abilities["ITN"] += 1
        if "术语" in desc_lower:
            abilities["术语"] += 1
        if "排版" in desc_lower:
            abilities["排版"] += 1
    print("\n能力覆盖:")
    for k, v in abilities.items():
        print(f"  {k}: {v} 条")


if __name__ == "__main__":
    print_stats()
